// Piece.java
public class Piece {
    private char symbol;  // The symbol representing the piece, e.g., 'X' or 'O'

    // Constructor
    public Piece(char symbol) {
        this.symbol = symbol;
    }

    // Getter for symbol
    public char getSymbol() {
        return symbol;
    }

    // Setter for symbol
    public void setSymbol(char symbol) {
        this.symbol = symbol;
    }
}
